using System;
using System.Collections.ObjectModel;

namespace Notes_App.MVVM.Model
{
    public class FolderClass
    {
        //___________________________________________________________________________________________________________
        //__________________________________________Parameters_______________________________________________________
        //___________________________________________________________________________________________________________

        /// <summary>
        /// stores the folder name
        /// </summary>
        public string FolderName { get; set; } = String.Empty;
        //___________________________________________________________________________________________________________
        /// <summary>
        /// stores the notes in the folder
        /// </summary>
        public ObservableCollection<NotesClass> Notes { get; set; } = new ObservableCollection<NotesClass>();
        //___________________________________________________________________________________________________________

        //___________________________________________________________________________________________________________
        //__________________________________________Constructors_____________________________________________________
        //___________________________________________________________________________________________________________

        /// <summary>
        /// constructor to make FolderClass Objects
        /// </summary>
        /// <param name="FN"></param>
        /// <param name="OC"></param>
        public FolderClass(String FN, ObservableCollection<NotesClass> OC)
        {
            this.FolderName = FN;
            this.Notes = OC;
        }
        //___________________________________________________________________________________________________________

        /// <summary>
        /// Default constructor
        /// </summary>
        public FolderClass()
        {
        }
        //___________________________________________________________________________________________________________

        //___________________________________________________________________________________________________________
        //_____________________________________________Methods_______________________________________________________
        //___________________________________________________________________________________________________________

        /// <summary>
        /// method to add notes to the folder
        /// </summary>
        /// <param name="NC"></param>
        public void AddNoteToFolder(NotesClass NC)
        {
            Notes.Add(NC);
        }
        //___________________________________________________________________________________________________________
    }
}
